// Favorite Number: Store your favorite number in a variable.
let myFavNum : number = 28


// Create a message that reveals your favorite number.
let message : string = `My favorite number is ${myFavNum}`
/*
A template literal (enclosed by backticks `) is used to create a string that includes the value of myFavNum.
The ${myFavNum} syntax allows you to embed the value of the variable directly within the string.
*/

// Print the message.
console.log(message);
